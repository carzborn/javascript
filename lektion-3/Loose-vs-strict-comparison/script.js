
let age = 25;

//l loose comparison
console.log("age == 25", age == 25);    // true
console.log('age == "25"', age == "25");  
console.log('age != 25', age != "25");
console.log('age != "25"', age != "25");

// strict comparison
console.log('age === 25', age === 25);
console.log('age === "25"', age === "25");
console.log('age !== 25', age !== 25);
console.log('age !== "25"', age !== "25");


